</div><div id="wb_element_instance143" class="wb_element" style=" line-height: normal;"><p class="wb-stl-normal"><span style="color:#212121;">المعهد التحضيري<br>
تتكون السنة الدراسية في المعهد التحضيري من ثلاث مراحل وفصلين, في كل فصل يختبر الطالب اما النجاح والرسوب يكون في نهاية الفصل الثاني. الدوام الرسمي هو مابين 5-6 ايام في الاسبوع لا تزيد المحاضرات عن اربع في اليوم الواحد ولمدة تسعون دقيقة مع الاستراحة القصيرة مابين المحاضرات. نهاية كل شهر تكون الاختبارات الدورية ومن خلالها يستطيع الطالب بجمع العلامات لتساعده في نهاية فصل لرفع علاماته في الامتحانات.<br>
في المرحلة الاولى من وصول الطالب الى المعهد التحضيري يوضع الطالب في صف يتكون الى حد اقصى من 10 طلاب لتعلم اسس ومبادئ اللغة الروسيه من الكتاب الاول, في المرحلة الثانية يأخذ الطالب الكتاب الثاني للغه الروسيه فهوا يحتوي على كيفية تكوين الجمل وتعلم القواعد السهلة في اللغه الروسية وهنا يتم اضافة المواد الاخرى كلا على حسب تخصصه أدب, هندسة او طب.  في منتصف هذه المرحلة تكون امتحانات افصل الاول, المرحلة الثالثة هي اللأخيره وتقوم بتأهيل الطالب لدخولة للجامعة بأخذه الكتاب الثالث والاخير في اللغة الروسية, في نهاية هذه المرحلة تكون امتحانات الفصل الثالني والنهائي. </span></p>

<p class="wb-stl-normal"><br><span style="color:#212121;">اللغه الروسية<br>
اللغة الروسية هي لغة الشعب الروسي ولغة المخاطبة بين شعوب روسيا المترامية الأطراف. وتنتمي اللغة الروسية إلى مجموعة اللغات السلافية الشرقية التي تضم الروسية والأوكرانية والبيلوروسية. وتعود اللغة الروسية الى عداد أكثر لغات العالم انتشارا ، وهي إحدى اللغات الرسمية الست في الأمم المتحدة وتعتبر لغة عمل في العديد من المنظمات والمؤتمرات الدولية.<br>
وتدرس اللغة الروسية في مدارس وجامعات العديد من بلدان العالم بما فيها العربية.<br>
وأخيرا اللغة الروسية هي لغة الكتاب والشعراء الروس العظام بوشكين وليرمنتوف وغوغول وتورغينيف وتولستوي ودوستويفسكي وتشيخوف وكثيرين غيرهم ، ربما غير معروفين بالنسبة للقارئ العربي لأن مؤلفاتهم غير مترجمة الى العربية.<br>
