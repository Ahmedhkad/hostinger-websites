<div id="wb_element_instance126" class="wb_element" style=" line-height: normal;"><h1 class="wb-stl-heading1"><font color="#212121">الحياة في روسيا</font></h1>
</div><div id="wb_element_instance127" class="wb_element" style=" line-height: normal;"><p class="wb-stl-normal"><span style="color:#212121;"><strong>الظروف المعيشية: </strong><br>
قرار الانتقال للدراسة في بلد اخر – هذا يعتبر من اصعب القرارات ويتطلب الاستعداد المسبق. تحتاج للتكيف مع مجموعه متنوعة من شروط حياتك الجديدة, بالسكان المحليين, الثقافة, المناخ و اللغه الروسية. <br>
نقدم لك اهم المعلومات التي تساعدك على الاستعداد.</span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><br><span style="color:#212121;"><strong>السكن:</strong><br>
توفر الجامعات الروسية سكن يشمل جميع الطلبة الروس والاجانب معاً بأسعار جداً مناسبة, السعر يختلف مابين المدن والجامعات ويعتمد على مستوى الراحة التي يتم تقديمها في سكن الجامعة, يمكنك ان تدفع لمدة شهر, او مرة واحده لكل فصل دراسي, اعتمادا على النظام الداخلي للجامعة. </span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><br><span style="color:#212121;"><strong>ألاسعار:</strong><br>
العملة الروسية – الروبل الروسي ₽. في بداية عام 2017 سعر الصرف الروبل هو تقريبي: <br>
1 دولار امريكي    -  60  روبل روسي<br>
1 يورو         -  73  روبل روسي <br>
على الاراضي الروسية يمكنك التعامل فقط بالروبل الروسي ₽. يمكن استبدال العملات (الدولار و اليور فقط) في بنك ترغب بالتعامل معه. ويتم قبول جميع البطاقات الائتمانية الدولية في كل مكان و بمختلف عملاتها. ويمكنك استلام المال من خارج روسيا عن طريق التحويل على البطاقة الائتمانية, او Western Union (الويسترن يونيون).<br>
في عام 2016 كان الحد الادنى للطالب للعيش في روسيا (تقريبا 170 $). لكن يجب ان نتذكر ان موسكو وسان بطرسبورغ فهي على اعلى مستوى في الاسعار عن المدن الاخرى. ويجدر الذكر إلى ان غالبا ما يتم منح الطلاب خصومات تصل إلى 50٪ في المتاحف والمقاهي والنوادي الرياضية، ومحلات الملابس وجميع وسائل المواصلات فتكون أرخص بعدة مرات.</span></p>

<p class="wb-stl-normal"><br><span style="color:#212121;"><strong>وقت الفراغ:</strong><br>
الحياة الطلابية نشطة جدا في الجامعات الروسية - سوف تكون قادر على المشاركة في المؤتمرات والمعارض الطلابية، وغيرها من الندوات العلمية والتعليمية. أيضاً يمكنك المشاركة في الألعاب الرياضية - يوجد قسم خاص في كل جامعة  لكرة السلة، كرة القدم، ولكرة الطائرة وغيرها. الحياة الثقافية هي أيضا غنية جدا، ومسابقات إبداعية ومسابقات الجمال والعروض من عرض KVN الترفيهي، والذي يتم إعداده من قبل الطلبة أنفسهم.<br>
وظائف للطلاب<br>
هناك بعض الشروط الأساسية للعمل للطلبة الأجانب في روسيا - تحتاج إلى أن تكون في السن القانونية (أكثر من 18 سنة) ويوجد لديك الإذن المناسب من وزارة الهجرة في وزارة الداخلية للعمل. الى اذا كان الطالب الاجنبي يعمل في نفس الجامعة او في الأجازه الصيفيه فأنهو لا يحتاج الى التصاريح, اما في جميع الحالات الأخرى لابد من الحصول على اذن خاص. </span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><br><span style="color:#212121;"><strong>التأمين والخدمات الطبية:</strong><br>
هناك نوعان من الخدمات الطبية في روسيا<br>
حكومي<br>
خاص <br>
يمكنك الحصول على الخدمات الطبية مجاناً في حالات الطورائ فقط. اما في الحالات الاخرى فأنك تحتاج للتأمين الصحي للمراجعة في المشفى. دون التأمين الصحي يمكنك المراجعه في المصحات و المستشفيات لكن هذا سيكون على حسابك الشخصي.<br>
تكلفة التأمين الصحي للطلبة الاجانب:<br>
يعتمد على المدينة وحجم الخدمات و المدة المطلوبة من ثلاث اشهر حتى سنة. يتراوح سعر التأمين الصحي مابين 60 $ حتى 200 $.</span></p>

<p class="wb-stl-normal"><br><span style="color:#212121;"><strong>التأشيرة</strong> <br>
كيفية الحصول على التأشيرة الروسية:<br>
اولاً يتم ارفاق طلب دعوة عن طريق شركة القلم الروسي وبعد الحصول على الدعوة, يكون طلب التأشيرة في السفارة او القنصلية الروسية الموجوده في بلدك.<br>
الخطوة الاولى. الحصول على الدعوة<br>
توفر لك شركة القلم الروسي هذه الخطوة على ان نقوم بتقديم طلب والتواصل مع الجامعة المطلوبة للحصول عليها بالوقت المحدد, ثم نرسل لك نسخه على البريد الالكتروني الخاص بك.<br>
        الخطوة الثانية. تحضير المستندات المطلوبة<br>
            بعد تلقي الدعوة تحتاج لمراجعة السفارة الروسية (القسم القنصلي) في بلدك, مع المستندات المطلوبة:<br>
جواز السفر (الأصل) ونسخة من الصفحة الرئيسية<br>
الدعوة الأصلية لدخول روسيا<br>
النموذج الإلكتروني للدعوة<br>
صور شخصية 5, 4*5 سم<br>
شهادة عدم وجود فيروس نقص المناعة البشري<br>
للمتقدمين تحت 18 سنة – موافقة الوالدين على السفر مصدقة من كاتب العدل<br>
الخطوة الثالثة. المقابلة في السفارة او القنصلية الروسية<br>
بعدما تجمع كافة المستندات يتوجب عليك ان تأخذ موعدا للمقابلة مع القنصل في بلدك, بعد الموافقة واعطاء المستندات للقنصلية يجب عليك ان تدفع تكلفة تأشيرة الطالب. الوقت لتجهيز تأشيرة الطالب اسبوعين الى ثلاثة اسابيع.<br>
مدة صلاحية تأشيرة الطالب – عادتاً ماتكون لمدة ثلاث اشهر </span><br>
